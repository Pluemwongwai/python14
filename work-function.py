#คำสั่งหาพื้นที่สี่เหลี่ยม
def squre(width,height):
    ans = width*height
    print(ans)
squre(5,5)
#คำสั่งหาพื้นที่สามเหลี่ยม
def triangle(base,height):
    ans = 1/2*base*height
    print(ans)
triangle(10,5)
#คำสั่งหาพื้นที่วงกลม
def circle(radius):
    ans = 22/7*radius*radius
    print(ans)
circle(7)
